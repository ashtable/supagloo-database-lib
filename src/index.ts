export type { SecretCryptoErrorCode } from "./secrets";

// Shared GitHub App primitives (design-delta §2.3/§6a). App-JWT signing +
// installation-token minting, shared so API (callback verify + repo listing) and
// DBOS (git-ops workflows) use one implementation. Never persists a token.
export { signAppJwt, mintInstallationToken, GithubAppError } from "./github";
export type {
  GithubAppErrorCode,
  SignAppJwtOptions,
  MintInstallationTokenOptions,
  InstallationToken,
} from "./github";

// Shared S3 object-key layout helpers (design-delta §4/§8). One source of truth for
// the key format, shared by the writers (DBOS render/git-ops workflows) and the
// reader (the API's presigned-download route) so the format can never drift.
export {
  buildAssetKey,
  buildRenderOutputKey,
  buildRenderThumbnailKey,
  parseS3Key,
} from "./s3-keys";
export type { ParsedS3Key } from "./s3-keys";

// Shared semver parse/compare helpers (design-delta §2.6). Free-form,
// non-zero-padded `ProjectVersion.semver` must be ordered numerically (0.10.0 is
// newer than 0.2.0). Shared so the API version listing (#14) and the DBOS
// next-semver bump (#22) order/compute versions the same way.
export { parseSemver, compareSemver } from "./semver";
export type { ParsedSemver } from "./semver";

// Shared domain Zod schemas (design-delta §2.11): LLM structured-output contracts,
// the supagloo.project.json manifest format, audio/render request shapes, and the
// Prisma-enum mirrors. Schema consts are `*Schema`-suffixed; inferred TS types use
// the bare names.
export * from "./schemas";

// Shared ProjectJob.stages progress-log contract (design-delta §2.9/§6b/§7). The API
